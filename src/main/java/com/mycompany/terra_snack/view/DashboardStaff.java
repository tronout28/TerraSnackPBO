    private void loadJumlahPesanan() {

        List<Pesanan> list = pesananDAO.getAllPesanan();

        lblJumlahPesanan.setText(
                String.valueOf(list.size()));
    }

    private void loadTablePesanan() {

        DefaultTableModel model = (DefaultTableModel) tbDaftarPesanan.getModel();

        model.setRowCount(0);

        List<Pesanan> list = pesananDAO.getAllPesanan();

        int no = 1;

        for (Pesanan p : list) {

            model.addRow(new Object[] {
                    no++,
                    p.getPesananId(),
                    p.getTanggalPesanan(),
                    p.getPelangganId(),
                    "-",
                    p.getStatusPesanan()
            });
        }
    }

    private void loadAktivitas() {

        areaAktivitasTerbaru.setText("");

        List<Pesanan> list = pesananDAO.getAllPesanan();

        for (Pesanan p : list) {

            areaAktivitasTerbaru.append(
                    "Pesanan ID "
                            + p.getPesananId()
                            + " : "
                            + p.getStatusPesanan()
                            + "\n");
        }
    }

