    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSearchActionPerformed
        String status = cbStatusFilter.getSelectedItem().toString();

        DefaultTableModel model = (DefaultTableModel) tbDaftarPesanan.getModel();

        model.setRowCount(0);

        List<Pesanan> list = pesananDAO.getAllPesanan();

        int no = 1;

        for (Pesanan p : list) {

            if (status.equals("SEMUA")
                    || p.getStatusPesanan().equalsIgnoreCase(status)) {

                model.addRow(new Object[] {
                        no++,
                        p.getPesananId(),
                        p.getTanggalPesanan(),
                        p.getPelangganId(),
                        "-",
                        p.getStatusPesanan()
                });
            }
        }
    }// GEN-LAST:event_btnSearchActionPerformed

