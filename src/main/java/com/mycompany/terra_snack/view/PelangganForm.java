        private void loadPelangganData() {
            // Clear table
            tableModel.setRowCount(0);
            daftarPelanggan.clear();

            String sql = "SELECT * FROM pelanggan ORDER BY pelanggan_id ASC";

            try (Connection conn = DatabaseConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {

                int no = 1;
                while (rs.next()) {
                    Pelanggan p = new Pelanggan();
                    p.setPelangganId(rs.getInt("pelanggan_id"));
                    p.setNama(rs.getString("nama"));
                    p.setAlamat(rs.getString("alamat"));
                    p.setNoHp(rs.getString("no_hp"));

                    // Handle email if column exists
                    String email = "";
                    try {
                        email = rs.getString("email");
                    } catch (SQLException e) {
                        email = "-";
                    }

                    daftarPelanggan.add(p);

                    Object[] row = {
                        no++,
                        "PLG-" + String.format("%04d", p.getPelangganId()),
                        p.getNama(),
                        email != null ? email : "-",
                        p.getNoHp(),
                        p.getAlamat()
                    };
                    tableModel.addRow(row);
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,
                        "Error loading data: " + e.getMessage(),
                        "Database Error",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }

        /**
         * Load data ke form berdasarkan row yang dipilih
         */
        private void loadSelectedDataFromRow() {
            int[] selectedRows = JTPelanggan.getSelectedRows();

            // Jika hanya 1 baris yang dipilih, load ke form
            if (selectedRows.length == 1) {
                int row = selectedRows[0];
                String idPelangganStr = tableModel.getValueAt(row, 1).toString();
                int pelangganId = Integer.parseInt(idPelangganStr.replace("PLG-", ""));

                for (Pelanggan p : daftarPelanggan) {
                    if (p.getPelangganId() == pelangganId) {
                        Jpelanggan.setText("PLG-" + String.format("%04d", p.getPelangganId()));
                        JNPelanggan.setText(p.getNama());
                        JNoHP.setText(p.getNoHp());
                        JAlamat.setText(p.getAlamat());

                        // Load email
                        String email = tableModel.getValueAt(row, 3).toString();
                        JEmail.setText(email.equals("-") ? "" : email);
                        break;
                    }
                }
            } else if (selectedRows.length > 1) {
                // Jika lebih dari 1, kosongkan form (karena akan digunakan untuk hapus massal)
                Jpelanggan.setText("");
                JNPelanggan.setText("");
                JEmail.setText("");
                JNoHP.setText("");
                JAlamat.setText("");
            }
        }
        
         private void tambahPelanggan() {
        // Pastikan tidak ada row yang dipilih saat menambah
        if (JTPelanggan.getSelectedRowCount() > 0) {
            JOptionPane.showMessageDialog(this,
                "Batal pilih data terlebih dahulu sebelum menambah data baru!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String nama = JNPelanggan.getText().trim();
        String email = JEmail.getText().trim();
        String noHp = JNoHP.getText().trim();
        String alamat = JAlamat.getText().trim();

        // Validasi
        if (nama.isEmpty() || noHp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Nama, No. HP, dan Alamat harus diisi!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!noHp.matches("08[0-9]{8,11}")) {
            JOptionPane.showMessageDialog(this,
                "Nomor HP harus dimulai dengan 08 dan 10-13 digit!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "INSERT INTO pelanggan (nama, email, no_hp, alamat) VALUES (?, ?, ?, ?)";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, nama);
            ps.setString(2, email.isEmpty() ? null : email);
            ps.setString(3, noHp);
            ps.setString(4, alamat);

            int rowsAffected = ps.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this,
                    "Pelanggan berhasil ditambahkan!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);
                loadPelangganData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this,
                    "Gagal menambahkan pelanggan!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

