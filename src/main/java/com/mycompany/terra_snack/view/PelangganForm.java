        if (nama.isEmpty() || noHp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Nama, No. HP, dan Alamat harus diisi!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!noHp.matches("08[0-9]{8,11}")) {
            JOptionPane.showMessageDialog(this,
                "Nomor HP harus dimulai dengan 08 dan 10-13 digit!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
             int row = selectedRows[0];
        String idPelangganStr = tableModel.getValueAt(row, 1).toString();
        int pelangganId = Integer.parseInt(idPelangganStr.replace("PLG-", ""));

